package day0318;

public class Ex2StringCompare {

	public static void main(String[] args) {

		System.out.println("Apple".compareTo("Apple")); //같을 경우 0
		System.out.println("Apple".compareTo("Banana")); // -1
		System.out.println("Hello".compareTo("Hi")); // e하고 i가 아스키코드 차이가 몇이냐.. 
		System.out.println("Hello".compareTo("Apple")); //7 (H와 A 비교)
		
	
		
	}

}
